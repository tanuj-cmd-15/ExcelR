public class Comparison {
    public static void main(String[] args) {
        // Comparison =================================
        int x=10, y =21;
        System.out.println(x==y);

        //comparison !=    
        System.out.println(x!=y);

        //Greater than >= 
        System.out.println(x>y);
        System.out.println(x>=y);

        //smaller than < <=  
        System.out.println(x<y);
        System.out.println(x<=y);

    }
}
